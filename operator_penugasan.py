
# Ambil input untuk mengisi nilai
a = int(input("Inputkan nilai a: "))

# tambahkan dengan 2
a += 2
print ("Nilai a += 2 adalah %d" % a)

# kurangi 3
a -= 3
print ("Nilai a -= 3 adalah %d" % a)

# kali 10
a *= 10
print ("Nilai a *= 10 adalah %d" % a)

# bagi dengan 4
a /= 4
print ("Nilai a /= 4 adalah %d" % a)

# pangkat 10
a **= 10
print ("Nilai a **= 10 adalah %d" % a)

# Berapakah nilai a sekarang?
print ("--> Nilai a adalah %d" % a)